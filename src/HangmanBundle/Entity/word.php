<?php

namespace HangmanBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * word
 *
 * @ORM\Table(name="word")
 * @ORM\Entity(repositoryClass="HangmanBundle\Repository\wordRepository")
 */
class word
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="word", type="string", length=11, unique=true)
     */
    private $word;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set word
     *
     * @param string $word
     *
     * @return word
     */
    public function setWord($word)
    {
        $this->word = $word;

        return $this;
    }

    /**
     * Get word
     *
     * @return string
     */
    public function getWord()
    {
        return $this->word;
    }
}

